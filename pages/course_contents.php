<h1>Course Contents</h1>

<h2><a name="calculus">Calculus</a></h2>
<p>
    Technical science uses mathematics as a language to describe phenomena and to solve problems. With trigonometry one
    can describe waves. In many problems an optimal setting of parameters should be found. This can be done with
    mathematical techniques such as differentiation. Integral calculus is needed for the calculation of a center of
    gravity. Many models use differential equations. In this course you will learn these and other basic techniques with
    applications.
</p>

<h2><a name="programming">Programming</a></h2>
<p>
    This course aims to provide students with an understanding of the role computation can play in
    solving problems. It also aims to help students, regardless of their major, to feel justifiably confident
    of their ability to write small programs that allow them to accomplish useful goals. The course uses
    the Java programming language.
</p>

<h2><a name="psytech">Introduction Psychology & Technology</a></h2>
<p>
    Are you interested in understanding the human mind, how we think and perceive and how our
    brains work? This course introduces you in the fascinating world of psychology. You will
    learn about motivation and emotions and other factors shaping human behavior, including the
    social nature of the human 'animal'. Psychological knowledge will improve your
    understanding how to create technological systems and environments that fit human
    capabilities and serve people's needs.
</p>

<h2><a name="physics">Applied Physical Sciences</a></h2>
<p>
    This course will give future engineers the conceptual basis of a few fundamental concepts of
    physics, with which they can form a "founded" idea of both scientific and technological
    developments in the fields of medical, nano and energy technology, climate control etc. The
    importance of the concepts that will be treated, will be cleared up using engaging
    demonstrations and examples in which the above named technologies are important.
</p>

<h2><a name="logic">Logic & Set Theory</a></h2>
<p>
    Logical reasoning is an indispensable tool when designing a solution to any complex
    technical problem. This course discusses the principles of correct logical reasoning. You
    learn to formulate statements in unambiguous logical language, and to manipulate statements
    in a structured and logically valid manner. At the end of the course you are able to give
    simple mathematical proofs, in particular using the technique of mathematical induction.
</p>

<h2><a name="modelling">Introduction to modelling</a></h2>
<p>
    Engineers solve problems, and use model to do so. This always requires a translation to
    rephrase the initial problem in mathematical terms. A second translation comes in to
    interpret the mathematical result in the context of the initial problem. This course introduces
    methods and techniques to perform such translations in a structured manner.
</p>

<h2><a name="computer_systems">Computer Systems</a></h2>
<p>
    Introduction to Computer Systems, on the structure and working of general-purpose machines
    that can execute computer programs, spanning the conceptual gap between simple switches
    on the one hand and the abstract machine that constitutes a "computer" on the other.
</p>

<h2><a name="hypermedia">DBL Hypermedia</a></h2>
<p>
    In this course students will learn: - about the concepts of hypermedia (in a Web context), -
    how to use wiki tools for creating own wiki sites, - how to work efficiently in a group.
</p>

<h2><a name="data_structures">Data Structures</a></h2>
<p>
    For solving algorithmic problems many aspects need to be mastered: efficient ways of storing
    and manipulating (large amounts of) data, algorithm design techniques, how to establish that
    an algorithm is correct, and how to analyze the efficiency of an algorithm.
</p>

<h2><a name="design">Design</a></h2>
<p>
    Anyone can learn how to design. The basic Design course consists of doing a design project,
    supported by (web) lectures, weekly assignments, workshops and coaching. The scope of all
    design projects is determined by a central theme that allows student teams to specify their
    own design challenge or problem, their own design and solution space, and their own design
    process. Solutions for the design problem or opportunity require multidisciplinary input. This
    year's central theme is "Zero Energy".
</p>

<h2><a name="discrete_structures">Discrete Structures</a></h2>
<p>
    Underlying structures in computer science are often discrete: they are finite, or represented by
    integer numbers, in contrast to continuous structures like real numbers. Typical discrete
    structures are graphs, representing all kinds of networks. In this course we study graphs,
    relations, orderings, functions on finite sets, counting arguments and some basic number
    theory. As all of these topics require formal reasoning, in this course there is quite some
    emphasis on giving formal proofs.
</p>

<h2><a name="data_modelling">Data modelling & databases</a></h2>
<p>
    2ID50 Data modeling and databases Our lives are awash in data (e.g., social, business, web,)
    which only continues to grow in both quantity and variety. Database management systems
    are the key technologies which facilitate our practical use of these massive data sets. In this
    course, we study fundamental concepts, such as data model design and formulation of queries
    against databases, which underpin the effective practical use of industrial strength data
    management systems.
</p>

<h2><a name="algorithms">DBL Algorithms</a></h2>
<p>
    How can we automatically put labels (like city names) on a map in such that the labels do not
    overlap? How can we compute train schedules that optimally use the available tracks and
    trains? How can we reconstruct 3D objects from laser scans? These are just a few examples
    of challenging problems that require efficient algorithmic solutions and effective software
    implementations of those solutions. In this course you will attack such an algorithmic
    problem in a team of five or six students.
</p>

<h2><a name="programming_methods">Programming Methods</a></h2>
<p>
    Modern programming languages offer powerful facilities for the design of object-oriented
    programs. These facilities must be used in a methodical way to avoid chaos and
    unmaintainable software. The design techniques concern both the way of working (design
    process) and the organization of the software itself. The course focuses on abstraction
    mechanisms (procedural, data), design patterns, and underlying design principles. Test-driven
    development (TDD) is used as design process.
</p>

<h2><a name="software_specification">Software specification & architecture</a></h2>
<p>
    Software specification is a collection of techniques for describing software systems, their
    organization and behavior. Software architecture takes these ideas further and focuses on
    large systems, consisting of multiple components, relations between these components and
    their evolution. We discuss a number of important approaches to software specification and
    architecture, their possibilities and constraints, resulting in guidelines for their application.
</p>

<h2><a name="networks">Computer Networks & Security</a></h2>
<p>
    Interactivity is key for modern ICT systems. A computer network is an infrastructure of
    interconnected devices and a facilitator of distributed applications. Yet interactivity comes
    with security threats. This course covers the organization of computer networks (e.g. the
    Internet), basics of security and main network and security protocols. Students learn to
    explain solutions to standard problems and to analyze simple protocols with respect to
    correctness, performance, reliability and security.
</p>

<h2><a name="probability">Probability & Statistics</a></h2>
<p>
    This course gives an introduction to probability and statistics. In many situations it is
    impossible to predict how a complex system will behave. Models capturing this uncertainty
    are essential to study such systems. Probability theory gives us the tools to do this in a
    powerful manner. Statistics, on the other hand, concerns what can be learned from data. In
    this course you will learn how to build probabilistic models, perform statistical analyses of
    real data, and the theory behind all this.
</p>

<h2><a name="bus_information">Bus. Information systems</a></h2>
<p>
    In most organizations the IT function is completely intertwined with the other business
    functions. This course addresses this relation. In particular, it discusses the impact that the IT
    function has on the performance and competitiveness of the organization and it presents tools
    to model and manage the relation between the IT function and the other business functions.
</p>

<h2><a name="operating_systems">Operating Systems</a></h2>
<p>
    This course studies fundamental design and implementation ideas in the engineering of operating
    systems. Lectures are based on a study of UNIX and research papers. Topics include virtual memory,
    threads, context switches, kernels, interrupts, system calls, interprocess communication,
    coordination, and the interaction between software and hardware. Individual laboratory
    assignments involve implementation of a small operating system in C, with some x86 assembly.
</p>

<h2><a name="web_analytics">Web analytics</a></h2>
<p>
    In Web Analytics course we focus on the utility of different technologies for understanding
    the Web as a whole, for modeling the behavior of the individual Web users and user groups,
    for using Web as an experimentation platform. We will cover the topics of OLAP style data
    exploration, data mining, and social network analysis. We will give a special attention to
    understanding search, recommendations and advertisements on the Web.
</p>