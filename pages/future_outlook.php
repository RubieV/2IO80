<h2><a name="government">Government Agencies</a></h2>
<p>
    Perhaps they are looking for a data scientist that helps with leading a team of other scientists involved in data.
    Or maybe they are looking to hire an entry-level data scientist to develop new algorithms and find relationships
    within existing databases.
</p>

<h2><a name="tech">Technology Companies</a></h2>
<p>
    It might be that they are hiring a data scientist to help drive a company's strategy as it analyzes data from its 70
    million customers worldwide. Maybe they want you in a Big Data analytics division or something like it.
</p>

<h2><a name="consult">Consulting & Research Firms</a></h2>
<p>
    It can be that someone wants a data scientist to help manage its unstructured database, to help identify new
    insights for its clients. It could be that someone would like to hire a data scientist to increase its understanding
    of customer behaviour, category insights and market trends to better assist its retail clients.
</p>

<h2><a name="science">Scientific Organizations</a></h2>
<p>
    A company may be looking for Data Scientist to help deal with huge amounts of biomedical data they generate.
</p>

<hr/>
<p>
    In short, it does not matter what field you would like to pick. Data scientist is a still new and young profession.
    And by the looks of it the demand for them is still growing.
</p>

<p>
    You could subdivide data scientists in four different categories:
</p>
<ul>
    <li>
        Data Businesspeople are the product and profit-focused data scientists. They're leaders, managers, and
        entrepreneurs, but with a technical bent.
    </li>
    <li>
        Data Creatives are eclectic jacks-of-all-trades, able to work with a broad range of data and tools. They may
        think of themselves as artists or hackers, and excel at visualization and open source technologies.
    </li>
    <li>
        Data Developers are focused on writing software to do analytic, statistical, and machine learning tasks, often
        in production environments. They often have computer science degrees, and often work with so-called big data.
    </li>
    <li>
        Data Researchers apply their scientific training, and the tools and techniques they learned in academia, to
        organizational data. They may have PhDs, and their creative applications of mathematical tools yields valuable
        insights and products.
    </li>
</ul>
