<h1>Electives</h1>
<p>
    45 ECTS are (almost) entirely free to choose during the <a href="?id=3">bachelor college</a> program. There are certain restrictions to
    keep the electives coherent and provide the student with valuable additional information. To do this, there are
    elective-packages that make it easy for the student to choose a consistent set of electives.
</p>