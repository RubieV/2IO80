<h2>Exploration, Specialization, Application</h2>
<p>
    In each USE package there is one main subject. This subject makes the interaction between technology and the
    surrounding environment clearly visible and focuses on one or more USE perspectives. A USE package is made up of
    three components of 5 ECTS each.
</p>

<h3>Exploration</h3>
<p>
    In this phase you explore the subject and acquaint yourself with the main insights and discussions relating to the
    subject. In addition, you conduct a number of case studies in which you study the interaction between technology and
    the surrounding environment. The case studies can be related to various areas of technology.
</p>

<h3>Specialization</h3>
<p>
    Here you become acquainted with knowledge (theories, methods, and techniques) of relevant elements of the social and
    management sciences and the humanities. Deepening your package often means choosing various subjects that deepen
    your knowledge.
</p>

<h3>Application</h3>
<p>
    Lastly, you apply the knowledge and skills you have learned to a project within a relevant area of technology. These
    projects can be design or research oriented. For one learning pathway, several projects are offered by various
    departments. This allows you to make connections with a variety of areas of technology.
</p>