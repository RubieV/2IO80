<h1>Basic Courses</h1>
<p>
    Every student takes a number of basic courses. These basic courses provide the foundation for moulding the
    'Eindhoven Engineer', and will develop your transversal knowledge. This is the knowledge you will need to follow
    electives and coherent elective packages in a degree program outside the one you have chosen.
</p>
<p>
    The basic courses include Mathematics, Natural Sciences, Modeling, Design, User, Society & Enterprise, and
    Professional Skills. There are different variants of the basic courses for different degree programs.
</p>
<h2>Changing Major</h2>
<p>
    When you change your Major during your Bachelor, it is possible you followed an other Basic Course.
    Speak to your lecturer or coach in this case, because it is possible you have to gain some knowledge to succesfully
    complete your new Major.
</p>